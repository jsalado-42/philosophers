#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)

{
	size_t				i;
	const unsigned char	*ucs1;
	const unsigned char	*ucs2;

	i = 0;
	ucs1 = (const unsigned char *)s1;
	ucs2 = (const unsigned char *)s2;
	while (i < n)
	{
		if (ucs1[i] != ucs2[i])
			return (ucs1[i] - ucs2[i]);
		i++;
	}
	return (0);
}
