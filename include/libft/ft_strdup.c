#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	int		len;
	char	*result;

	len = ft_strlen((char *)s);
	result = malloc(sizeof(char) * (len + 1));
	if (!result)
		return (NULL);
	i = 0;
	while (s[i])
	{
		result[i] = s[i];
		i++;
	}
	result[i] = 0;
	return (result);
}
